/**
 * Get translations for a given language code.
 * Falls back to English if the language is not supported.
 */
export function t(languageCode: string): Translations {
  return translations[languageCode] || translations.en;
}

/**
 * Replace {name} placeholder in a translation string.
 */
export function tReplace(str: string, replacements: Record<string, string>): string {
  let result = str;
  for (const [key, value] of Object.entries(replacements)) {
    result = result.replace(`{${key}}`, value);
  }
  return result;
}
